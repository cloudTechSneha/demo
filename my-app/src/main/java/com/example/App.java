package com.example;

/**
 * Simple App class for demo purposes.
 */
public final class App {

    /**
     * Adds two numbers.
     */
    public int subtract(final int a, final int b) {
        return a - b;
    }

    /**
     * Subtracts b from a.
     */
    public int add(final int a, final int b) {
        return a + b;
    }
}
